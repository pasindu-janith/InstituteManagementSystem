    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            ResultSet rs = DB.search("SELECT classcode,SUM(subtotal) AS sumtotal,SUM(discount) AS sumdiscount FROM invoice_class WHERE paymentmonth='"
                    + DateAndTime.getYearMonth() + "' AND classcode!='null' GROUP BY classcode");
            while (rs.next()) {
                IncomeDto income = new IncomeDto();
                ResultSet rs_class = DB.search("SELECT * FROM class c "
                        + "INNER JOIN teacher t ON c.teacher=t.nic "
                        + "INNER JOIN subject s ON c.subject=s.id "
                        + "INNER JOIN classtype ct ON c.classtype=ct.id "
                        + "WHERE c.classcode='" + rs.getString("classcode") + "'");
                String classCode = rs.getString("classcode");
                String className = "";
                if (rs.getString("classcode") == null) {
                    classCode = "0000";
                    className = "Admission";
                }
                if (rs_class.next()) {
                    className = "Grade "
                            + rs_class.getString("c.grade") + " " + rs_class.getString("s.name") + " "
                            + rs_class.getString("ct.type") + " - "
                            + rs_class.getString("t.fname") + " " + rs_class.getString("t.lname");
                }

                income.setClassName(FormatName.getFormatName(className));

                income.setClassCode(classCode);
                income.setSubtotal(rs.getDouble("sumtotal"));
                income.setDiscount(rs.getDouble("sumdiscount"));
                income_list.add(income);
            }
            JRBeanCollectionDataSource incomeDataSource = new JRBeanCollectionDataSource(income_list);
            Map<String, Object> params = new HashMap<>();
            params.put("class_data", incomeDataSource);
            params.put("param_month", DateAndTime.getYearMonth());
            params.put("db_data", DB.getConnectionDB());
            JREmptyDataSource dataSource = new JREmptyDataSource();
            InputStream stream = new FileInputStream("src/com/institute/report/ClassIncomeReport.jasper");
            JasperPrint fillReport = JasperFillManager.fillReport(stream, params, dataSource);
            JasperViewer.viewReport(fillReport, false);
            income_list = new ArrayList<>();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            ResultSet rs = DB.search("SELECT classcode,SUM(subtotal) AS sumtotal,SUM(discount) AS sumdiscount FROM invoice_class WHERE paymentmonth='"
                    + DateAndTime.getYearMonth() + "' AND classcode in (SELECT classcode FROM class WHERE teacher='"
                    + combo_teacher.getSelectedItem().toString().split("-")[0] + "') GROUP BY classcode");
            while (rs.next()) {
                TeacherDto teacher = new TeacherDto();
                ResultSet rs_class = DB.search("SELECT * FROM class c "
                        + "INNER JOIN subject s ON c.subject=s.id "
                        + "INNER JOIN classtype ct ON c.classtype=ct.id "
                        + "WHERE c.classcode='" + rs.getString("classcode") + "'");
                if (rs_class.next()) {
                    teacher.setClassName(FormatName.getFormatName("Grade "
                            + rs_class.getString("c.grade") + " " + rs_class.getString("s.name") + " "
                            + rs_class.getString("ct.type")));
                    teacher.setFee(rs_class.getDouble("c.fee"));
                }
                ResultSet rs_stud = DB.search("SELECT COUNT(*) AS studcount FROM student_class WHERE classcode='"
                        + rs.getString("classcode") + "' AND status='1'");
                if (rs_stud.next()) {
                    teacher.setRegStudents(rs_stud.getInt("studcount"));
                }
                teacher.setClassCode(rs.getString("classcode"));
                teacher.setTotal(rs.getDouble("sumtotal"));
                teacher.setDiscount(rs.getDouble("sumdiscount"));
                teacher_list.add(teacher);

            }
            JRBeanCollectionDataSource incomeDataSource = new JRBeanCollectionDataSource(teacher_list);
            Map<String, Object> params = new HashMap<>();
            params.put("teacher_data", incomeDataSource);
            params.put("param_month", DateAndTime.getYearMonth());
            params.put("db_data", DB.getConnectionDB());
            params.put("param_teacher", combo_teacher.getSelectedItem().toString().split("-")[0]);
            params.put("param_teachername", combo_teacher.getSelectedItem().toString().split("-")[1]);
            JREmptyDataSource dataSource = new JREmptyDataSource();
            InputStream stream = new FileInputStream("src/com/institute/report/TeacherReport.jasper");
            JasperPrint fillReport = JasperFillManager.fillReport(stream, params, dataSource);
            JasperViewer.viewReport(fillReport, false);
            teacher_list = new ArrayList<>();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

