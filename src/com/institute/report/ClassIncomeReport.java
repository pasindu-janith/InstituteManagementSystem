    private void loadTeachers() {
        try {
            ResultSet rs = DB.search("SELECT nic,fname,lname FROM teacher WHERE status='1' ORDER BY fname ASC");
            Vector v = new Vector();
            while (rs.next()) {
                v.add(rs.getString("nic") + "-" + rs.getString("fname") + " " + rs.getString("lname"));
            }
            combo_teacher.setModel(new DefaultComboBoxModel<>(v));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

