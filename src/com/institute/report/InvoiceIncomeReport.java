    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if ((rad_day.isSelected() || rad_month.isSelected()) && jTable1.getRowCount() != 0 ) {
                InputStream stream = new FileInputStream("src/com/institute/report/InvoiceIncomeReport.jasper");
                Map<String, Object> params = new HashMap();
                String type = "";
                if (rad_day.isSelected()) {
                    type = "Day Report";
                    params.put("param_reportfor", DateAndTime.getDate());
                } else if (rad_month.isSelected()) {
                    type = "Month Report";
                    params.put("param_reportfor", DateAndTime.getYearMonth());
                }
                params.put("param_reporttype", type);
                JRTableModelDataSource dataSource = new JRTableModelDataSource(jTable1.getModel());
                JasperPrint fillReport = JasperFillManager.fillReport(stream, params, dataSource);
                JasperViewer.viewReport(fillReport, false);
            } else {
                JOptionPane.showMessageDialog(this, "No Rows to Print Report");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

