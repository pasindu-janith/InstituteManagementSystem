    private void dataSearch(String date) {
        try {
            ResultSet rs = DB.search("SELECT * FROM invoice WHERE datetime LIKE '" + date + "%'");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("invoiceid"));
                v.add(rs.getString("studentid"));
                v.add(rs.getString("datetime"));
                v.add(rs.getString("user"));
                v.add(rs.getString("nettotal"));
                v.add(rs.getString("payment"));
                dtm.addRow(v);
            }
            calculateTotal();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void calculateTotal() {
        double total = 0.0;
        for (int row = 0; row < jTable1.getRowCount(); row++) {
            double subtotal = Double.parseDouble(jTable1.getValueAt(row, 4).toString());
            total += subtotal;
        }
        txt_total.setText("" + total);
    }
