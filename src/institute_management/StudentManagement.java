    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            ResultSet rs = DB.search("SELECT `studentid`,`fname`,`lname` FROM student WHERE `studentid`='"
                    + txt_studid.getText() + "'");
            if (rs.next()) {
                int confirmDialog = JOptionPane.showConfirmDialog(this, "Are you sure to delete student "
                        + rs.getString("studentid") + "-" + rs.getString("fname") + " " + rs.getString("lname"),
                        "Delete student", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,
                        design.getQuestionIcon());
                if (confirmDialog == JOptionPane.YES_OPTION) {
                    DB.push("UPDATE student SET `status`='0' WHERE `studentid`='" + txt_studid.getText() + "'");
                    DB.push("UPDATE student_class SET `status`='0' WHERE `studentid`='" + txt_studid.getText() + "'");
                    JOptionPane.showMessageDialog(this, "Student deleted successfully!", "",
                            JOptionPane.PLAIN_MESSAGE, design.getDeleteSuccessIcon());
                }
            } else {
                JOptionPane.showMessageDialog(this, "No registered student from thid ID", "Error",
                        JOptionPane.ERROR_MESSAGE, design.getErrorIcon());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

