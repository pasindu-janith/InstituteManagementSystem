    private void generateStudentId() {
        try {
            ResultSet rs = DB.search("SELECT COUNT(studentid) AS studentcount FROM student");
            if (rs.next()) {
                int studCount = rs.getInt("studentcount");
                txt_studid.setText(String.valueOf(100000 + studCount));
                txt_fname.grabFocus();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

