    private void loadNotes() {
        try {
            ResultSet rs = DB.search("SELECT * FROM note WHERE `status`='1'");
            Vector v = new Vector();
            while (rs.next()) {
                String note = rs.getString("note");
                v.add(note);
            }
            list_note.setListData(v);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

