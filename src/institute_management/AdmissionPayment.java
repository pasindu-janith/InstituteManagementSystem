    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int confirmDialog = JOptionPane.showConfirmDialog(this,
                    "Are you sure cancel the registration process! All data will be lost",
                    "Cancel Process", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,
                    design.getQuestionIcon());
            if (confirmDialog == JOptionPane.YES_OPTION) {
                DB.push("DELETE FROM student WHERE `studentid`='" + txt_studid.getText() + "'");
                smanage.setEnabled(true);
                this.dispose();
                smanage.addimssion = null;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void txt_admissionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_admissionActionPerformed
        if (!txt_admission.getText().equals("")) {
            double admission = Double.parseDouble(txt_admission.getText());
            txt_admission.setText("" + admission);
            txt_netadmission.setText("" + admission);
            txt_discount.setText(null);
            txt_discount.grabFocus();
        }
    }//GEN-LAST:event_txt_admissionActionPerformed

    private void txt_discountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_discountActionPerformed
        if (txt_discount.getText().equals("")) {
            txt_discount.setText("0.0");
        }
        double discount = Double.parseDouble(txt_discount.getText());
        double admissionFee = Double.parseDouble(txt_admission.getText());
        txt_netadmission.setText("" + (admissionFee - discount));
        txt_payment.grabFocus();
    }//GEN-LAST:event_txt_discountActionPerformed

    private void txt_paymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_paymentActionPerformed
        if(!txt_payment.getText().equals("")){
            double netadmission = Double.parseDouble(txt_netadmission.getText());
            double payment = Double.parseDouble(txt_payment.getText());
            if(payment >= netadmission){
                txt_payment.setText(""+payment);
                txt_balance.setText(""+(payment - netadmission));
                txt_balance.grabFocus();
            } else{
                JOptionPane.showMessageDialog(this, "Payment not enough!");
                txt_payment.grabFocus();
            }
        }
    }//GEN-LAST:event_txt_paymentActionPerformed

    private void txt_balanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_balanceActionPerformed
        try {
            DB.push("INSERT INTO invoice VALUES('"+
                    txt_invoiceid.getText()+"','"+
                    txt_studid.getText()+"','"+
                    DateAndTime.getDateTime()+"','"+
                    txt_netadmission.getText()+"','"+
                    txt_payment.getText()+"','"+
                    SystemConfigData.getUserid()+"')");
            ResultSet rs = DB.search("SELECT id FROM paymenttype WHERE type='ADMISSION'");
            if(rs.next()){
            DB.push("INSERT INTO invoice_class"
                    + "(`invoiceid`,`paymentmonth`,`fee`,`paymenttype`,`discount`,`subtotal`) VALUES('"+
                    txt_invoiceid.getText()+"','"+
                    DateAndTime.getYearMonth()+"','"+
                    txt_admission.getText()+"','"+
                    rs.getString("id")+"','"+
                    txt_discount.getText()+"','"+
                    txt_netadmission.getText()+"')");
            }
            JOptionPane.showMessageDialog(this, "Payment succeed, Student Registered successfully!",
                                "Success", JOptionPane.PLAIN_MESSAGE, design.getSuccessIcon());
            generateInvoice();
            smanage.setEnabled(true);
            smanage.clearFields();
            this.dispose();
            smanage.addimssion = null;
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_txt_balanceActionPerformed

