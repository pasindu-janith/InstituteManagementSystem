    private void generateInvoiceId() {
        try {
            ResultSet rs = DB.search("SELECT COUNT(invoiceid) AS idcount FROM invoice");
            if(rs.next()){
                int count = rs.getInt("idcount");
                txt_invoiceid.setText(""+(++count));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void generateInvoice() {
        try {
            InputStream stream = new FileInputStream("src/com/institute/report/InstituteAdmissionInvoice.jasper");
            Map<String, Object> params = new HashMap();
            params.put("param_invoiceid", txt_invoiceid.getText());
            params.put("param_studentname", FormatName.getFormatName(txt_studname.getText()));
            params.put("param_studentid", txt_studid.getText());
            params.put("param_fee", txt_admission.getText());
            params.put("param_discount", txt_discount.getText());
            params.put("param_netfee", txt_netadmission.getText());
            params.put("param_payment", txt_payment.getText());
            params.put("param_balance", txt_balance.getText());
            params.put("param_employeeid", SystemConfigData.getUserid());
            
            JREmptyDataSource dataSource = new JREmptyDataSource();
            JasperPrint fillReport = JasperFillManager.fillReport(stream, params, dataSource);
            JasperViewer.viewReport(fillReport, false);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
