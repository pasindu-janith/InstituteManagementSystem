    private void loadAnalyze() {
        try {
            ResultSet rs = DB.search("SELECT COUNT(*) AS studcount FROM student_class WHERE `registerdate` LIKE '"
                    + DateAndTime.getYearMonth() + "%' AND `status`='1'");
            if (rs.next()) {
                lbl_classregistraions.setText(rs.getString("studcount"));
            }

            rs = DB.search("SELECT COUNT(id) AS regcount FROM invoice_class WHERE "
                    + "`paymenttype`=(SELECT id FROM paymenttype WHERE `type`='ADMISSION') AND `paymentmonth`='"
                    + DateAndTime.getYearMonth() + "'");
            if (rs.next()) {
                lbl_newregistrations.setText(rs.getString("regcount"));
            }
            rs = DB.search("SELECT COUNT(*) AS invoicedcount FROM invoice WHERE `datetime` LIKE '" + DateAndTime.getDate() + "%'");
            if (rs.next()) {
                lbl_invoiceissued.setText(rs.getString("invoicedcount"));
            }
            rs = DB.search("SELECT COUNT(employee) AS usercount FROM systemuser WHERE `status`='1'");
            if (rs.next()) {
                lbl_systemusers.setText(rs.getString("usercount"));
            }
            rs = DB.search("SELECT COUNT(studentid) AS studentcount FROM student WHERE `status`='1'");
            if (rs.next()) {
                lbl_regstudents.setText(rs.getString("studentcount"));
            }
            rs = DB.search("SELECT COUNT(nic) AS teachercount FROM teacher WHERE `status`='1'");
            if (rs.next()) {
                lbl_teachers.setText(rs.getString("teachercount"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

