    private void btn_logoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_logoutActionPerformed
        int confirmOption = JOptionPane.YES_OPTION;
        if (empManage != null || smanage != null || tmanage != null || cmanage != null || payment != null
                || attendance1 != null || attendance2 != null || classRegistration != null || schedule != null) {
            confirmOption = JOptionPane.showConfirmDialog(this, "Some processes are in progress, Are you sure to logout!",
                    "Log out", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, design.getQuestionIcon());
        }
        if (confirmOption == JOptionPane.YES_OPTION) {
            if (attendance1 != null) {
                attendance1.dispose();
                attendance1 = null;
            }
            if (attendance2 != null) {
                attendance2.dispose();
                attendance2 = null;
            }
            if (smanage != null) {
                smanage.dispose();
                smanage = null;
            }
            if (tmanage != null) {
                tmanage.dispose();
                tmanage = null;
            }
            if (cmanage != null) {
                cmanage.dispose();
                cmanage = null;
            }
            if (payment != null) {
                payment.dispose();
                payment = null;
            }
            if (empManage != null) {
                empManage.dispose();
                empManage = null;
            }
            if (classRegistration != null) {
                classRegistration.dispose();
                classRegistration = null;
            }
            if (schedule != null) {
                schedule.dispose();
                schedule = null;
            }
            Login login = new Login();
            login.setVisible(true);
            SystemConfigData.setUserid(null);
            SystemConfigData.setUsername(null);
            SystemConfigData.setUsertype(null);
            this.dispose();
        }
    }//GEN-LAST:event_btn_logoutActionPerformed

