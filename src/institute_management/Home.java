    private void loadTodayProgress() {
        //today held classes
        try {
            DefaultTableModel dtm = (DefaultTableModel) table_todayprogress.getModel();
            dtm.setRowCount(0);
            dtm.setRowCount(0);
            ResultSet rs = DB.search("SELECT t.`fname`,t.`lname`,s.`name`,c.`starttime`,c.`endtime`,"
                    + "c.`grade`,c.`hall`,h.`hallname`,c.`classcode`,ct.`type`,h.`floor` FROM class c "
                    + "INNER JOIN subject s ON c.`subject`=s.`id` "
                    + "INNER JOIN teacher t ON c.`teacher`=t.`nic` "
                    + "INNER JOIN classtype ct ON c.`classtype`=ct.`id` "
                    + "INNER JOIN hall h ON c.`hall`=h.`hallno` "
                    + "WHERE c.`classday`='" + DateAndTime.getDateInWord()
                    + "' AND c.`endtime` < '" + DateAndTime.getTime() + "' ORDER BY c.`grade` ASC");

            while (rs.next()) {
                ResultSet rs_get = DB.search("SELECT `id` FROM attendance WHERE `classcode`='"
                        + rs.getString("c.classcode") + "' AND date='" + DateAndTime.getDate() + "'");
                Vector v = new Vector();
                short studentCount = 0;
                String classStat = "NOT HELD";
                if (rs_get.next()) {
                    classStat = "HELD";
                    ResultSet rs_count = DB.search("SELECT COUNT(*) AS studentcount FROM attendance_mark"
                            + " WHERE `attendanceid`='" + rs_get.getString("id") + "'");
                    if (rs_count.next()) {
                        studentCount = rs_count.getShort("studentcount");
                    }
                }
                v.add(rs.getString("c.grade"));
                v.add(rs.getString("s.name") + "-" + rs.getString("ct.type"));
                v.add(rs.getString("t.fname") + " " + rs.getString("t.lname"));
                v.add(rs.getString("c.starttime") + "-" + rs.getString("c.endtime"));
                v.add(rs.getString("c.hall"));
                v.add(classStat);
                v.add(studentCount);
                dtm.addRow(v);
            }

            ResultSet rs_next = DB.search("SELECT `classcode`,`id` FROM attendance WHERE classcode IN"
                    + " (SELECT `classcode` FROM class WHERE `classday`!='"
                    + DateAndTime.getDateInWord() + "') AND `date`='" + DateAndTime.getDate() + "'");
            while (rs_next.next()) {
                ResultSet rs_second = DB.search("SELECT t.`fname`,t.`lname`,s.`name`,c.`starttime`,"
                        + "c.`endtime`,c.`grade`,c.`hall`,h.`hallname`,c.`classcode`,ct.`type`,h.`floor` FROM class c "
                        + "INNER JOIN subject s ON c.`subject`=s.`id` "
                        + "INNER JOIN teacher t ON c.`teacher`=t.`nic` "
                        + "INNER JOIN classtype ct ON c.`classtype`=ct.`id` "
                        + "INNER JOIN hall h ON c.`hall`=h.`hallno` "
                        + "WHERE c.classcode='" + rs_next.getString("classcode") + "'");
                if (rs_second.next()) {
                    ResultSet rs_count = DB.search("SELECT COUNT(*) AS studentcount FROM attendance_mark "
                            + "WHERE `attendanceid`='" + rs_next.getString("id") + "'");
                    if (rs_count.next()) {
                        Vector v = new Vector();
                        int studentCount = rs_count.getInt("studentcount");
                        v.add(rs_second.getString("c.grade"));
                        v.add(rs_second.getString("s.name") + "-" + rs_second.getString("ct.type"));
                        v.add(rs_second.getString("t.fname") + " " + rs_second.getString("t.lname"));
                        v.add("");
                        v.add(rs_second.getString("c.hall"));
                        v.add("HELD");
                        v.add(studentCount);
                        dtm.addRow(v);
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

